
// "typeof" vai dizer qual é o tipo da variável!

// No JS, todo número é "number", mesmo decimal.
let idade = 16;
console.log(typeof idade);
console.log(typeof 16);

let valor = 1500.50;
console.log(typeof valor);


// Tipo Boolean - true or false! 1 e 0!
let estaChovendo = false;
console.log(typeof estaChovendo);


// O tipo String é todo tipo de texto.
let nome = "Yohanna";
console.log(typeof nome);


// Aspas sempre mostram texto!
console.log(1 + 1);
console.log("1" + 1);


// Tipo NaN - Not a Number! 
console.log(valor * "valor");
